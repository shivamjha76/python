# Ask user for name
name = input("what is your name? ")

# Ask user for age
age = int(input("what is your age? "))

# Ask user for city
city = input("what is your city? ")

# Ask user what they enjoy
hobby = input("what do you enjoy doing? ")

# Create output message
output = f"Hello {name}, you are {age} years old, live in {city}, and enjoy {hobby}."

# print output to screen
print(output)

# Note: f-strings (formatted string literals) are used here for easy string interpolation.

# we use input() function to take input from user.

# we use int() function to convert age input from string to integer.

# Finally, we print the formatted output using print() function.

# This is a simple interactive program that collects user information and displays it back in a friendly format.

