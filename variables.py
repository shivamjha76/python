# variables in Python 

# what is a variable?
# A variable is like a labeled box where you can store information. You give the box a name (the variable name) and put something inside it (the value).

# How to create a variable?
# You just pick a name and use the equals sign (=) to assign a value to it.

# Example:
age = 25
name = "Shivam"
height = 5.9
# Here, we created three variables: age, name, and height.
# You can use these variables later in your code:
print("Name:", name)
print("Age:", age)
print("Height:", height)

# Variable Naming Rules:
# 1. Must start with a letter (a-z, A-Z) or underscore (_)
# 2. Can contain letters, numbers (0-9), and underscores
# 3. Cannot be a reserved word (like print, if, else, etc.)
# 4. Case-sensitive (age and Age are different variables)

# Variable Types:
# 1. Integers: Whole numbers (e.g., 1, 2, 3)
# 2. Floats: Decimal numbers (e.g., 1.5, 2.75)
# 3. Strings: Text (e.g., "Hello", "Python")
# 4. Booleans: True or False values

# Changing Variable Values:
age = 26  # Now age is updated to 26
print("Updated Age:", age)
# You can change the value of a variable anytime by assigning a new value to it.
# That's the basics of variables in Python! They help you store and manage data in your programs.